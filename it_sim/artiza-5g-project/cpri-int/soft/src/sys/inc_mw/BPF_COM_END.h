#include "BPF_COM.h"

/********************************************************************************************************************/
/* Function Prototype                                                                                               */
/********************************************************************************************************************/

/* RRH-001 MD alpha)matsuhashi add start */
#define BPF_COM_END_BIGTOLITTLE16(i) \
i = (i<<8) | ((i>>8) & 0x00ff) ;

#define BPF_COM_END_BIGTOLITTLE32(i) \
i = (i<<24) | ((i<<8) & 0x00ff0000) | ((i>>8) & 0x0000ff00) | ((i>>24) & 0x000000ff) ;

#define BPF_COM_END_BIGTOLITTLE64(i) \
i = (i<<56) | ((i<<40) & 0x00ff000000000000) | \
((i<<24) & 0x0000ff0000000000) | ((i<<8) & 0x000000ff00000000) | \
((i>>8) & 0x00000000ff000000) |  ((i>>24) & 0x0000000000ff0000) |  \
 ((i>>40) & 0x000000000000ff00) | ((i>>56) & 0x00000000000000ff) 

#define BPF_COM_END_LITTLETOBIG16(i) \
i = (i<<8) | ((i>>8) & 0x00ff) ;

#define BPF_COM_END_LITTLETOBIG32(i) \
i = (i<<24) | ((i<<8) & 0x00ff0000) | ((i>>8) & 0x0000ff00) | ((i>>24) & 0x000000ff) ;

#define BPF_COM_END_LITTLETOBIG64(i) \
i = (i<<56) | ((i<<40) & 0x00ff000000000000) | \
((i<<24) & 0x0000ff0000000000) | ((i<<8) & 0x000000ff00000000) | \
((i>>8) & 0x00000000ff000000) |  ((i>>24) & 0x0000000000ff0000) |  \
 ((i>>40) & 0x000000000000ff00) | ((i>>56) & 0x00000000000000ff) 
/* RRH-001 MD alpha)matsuhashi add end */

